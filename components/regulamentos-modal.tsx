export function RegulamentosModal() {
  const [isOpen, setIsOpen] = useState(false)
  const [agreed, setAgreed] = useState(false)

  const handleClose = () => {
    if (agreed) {
      setIsOpen(false)
    }
  }

  const handleOpenChange = (open: boolean) => {
    if (!open && !agreed) {
      return
    }
    setIsOpen(open)
    if (open) {
      setAgreed(false)
    }
  }

  return (
    <Dialog open={isOpen} onOpenChange={handleOpenChange}>
      <DialogTrigger asChild>
        <Button variant="outline" size="sm" className="gap-2 text-xs">
          <FileText className="w-3.5 h-3.5" />
          Ver Regulamento Geral
        </Button>
      </DialogTrigger>
      <DialogContent 
        className="max-w-2xl max-h-[90vh] flex flex-col"
        onPointerDownOutside={(e) => {
          if (!agreed) {
            e.preventDefault()
          }
        }}
        onEscapeKeyDown={(e) => {
          if (!agreed) {
            e.preventDefault()
          }
        }}
      >
        <DialogHeader>
          <DialogTitle>Regulamentos da Mesa</DialogTitle>
          <DialogDescription>
            Leia atentamente todos os termos e regulamentos abaixo.
          </DialogDescription>
        </DialogHeader>
        
        {/* Área de scroll com os termos */}
        <div className="flex-1 overflow-y-auto pr-2 max-h-[55vh] border border-border rounded-lg p-4 bg-card/50">
          <div className="text-sm text-muted-foreground whitespace-pre-line leading-relaxed">
            {termosCompletos}
          </div>
        </div>

        {/* Checkbox única no final */}
        <div className="pt-4 border-t border-border mt-4">
          <div 
            className="flex items-center gap-3 cursor-pointer"
            onClick={() => setAgreed(!agreed)}
          >
            <Checkbox 
              id="termos" 
              checked={agreed}
              onCheckedChange={(checked) => setAgreed(checked === true)}
            />
            <label 
              htmlFor="termos" 
              className="cursor-pointer text-sm text-foreground"
            >
              Li e concordo com todos os termos e regulamentos acima
            </label>
          </div>
        </div>

        <Button 
          onClick={handleClose}
          disabled={!agreed}
          className="w-full gap-2 mt-4"
        >
          {agreed && <Check className="w-4 h-4" />}
          {agreed ? "Concordar e Fechar" : "Marque a caixa para continuar"}
        </Button>
      </DialogContent>
    </Dialog>
  )
}
